// স্ক্রিন লোড হলে বাটনগুলো তৈরি করার মেইন ফাংশন
function loadDiseaseButtons() {
    const container = document.getElementById('diseaseButtons');
    if(!container) return;
    
    container.innerHTML = '';
    
    diseaseData.forEach(disease => {
        const button = document.createElement('button');
        button.className = 'disease-btn';
        button.innerText = disease.name;
        button.addEventListener('click', function() {
            showDetails(disease, button);
        });
        container.appendChild(button);
    });
}

// ক্লিক করলে কালারিং বক্সে তথ্য দেখানোর ফাংশন
function showDetails(disease, clickedButton) {
    const buttons = document.querySelectorAll('.disease-btn');
    buttons.forEach(btn => btn.classList.remove('active'));
    
    if(clickedButton) clickedButton.classList.add('active');

    const detailsContainer = document.getElementById('detailsContainer');
    
    detailsContainer.innerHTML = `
        <div class="details-card">
            <h2>${disease.name}</h2>
            
            <div class="info-block todo">
                <h3>👇 কী করণীয় (প্রাথমিক পদক্ষেপ):</h3>
                <p>${disease.todo}</p>
            </div>
            
            <div class="info-block medicine">
                <h3>💊 প্রাথমিক কী ঔষধ খাওয়া যাবে:</h3>
                <p>${disease.medicine}</p>
            </div>
            
            <div class="info-block warning">
                <h3>⚠️ কখন অবশ্যই ডাক্তারের কাছে যাবেন:</h3>
                <p>${disease.warning}</p>
            </div>
        </div>
    `;
}

// সার্চ করার ফাংশন
function searchDisease() {
    const query = document.getElementById('searchInput').value.toLowerCase().trim();
    if(query === "") return;
    
    const result = diseaseData.find(d => d.name.toLowerCase().includes(query));
    
    if (result) {
        showDetails(result, null);
    } else {
        document.getElementById('detailsContainer').innerHTML = `
            <div class="welcome-msg" style="color: #e74c3c;">
                <span>😥</span>
                <h3>দুঃখিত!</h3>
                <p>"${query}" এই রোগটির তথ্য খুঁজে পাওয়া যায়নি। সঠিক বানান দিয়ে আবার চেষ্টা করুন।</p>
            </div>
        `;
    }
}

